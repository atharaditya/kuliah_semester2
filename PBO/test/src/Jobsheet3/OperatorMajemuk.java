package Jobsheet3;

public class OperatorMajemuk {
    public static void main(String[] args) {
        int x;
        x=12;
        System.out.println("x+=3 "+(x+=3));
        System.out.println("x-=3 "+(x-=3));
        System.out.println("x*=3 "+(x*=3));
        System.out.println("x/=3 "+(x/=3));
        System.out.println("x%=3 "+(x%=3));
    }
    
}
