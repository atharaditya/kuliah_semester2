package Jobsheet3;

public class OpertorRelational {
    public static void main(String[] args) {
        int x,y,z;
        x=2; y=3; z=2;
        System.out.println("Operator ==");
        System.out.println("x==y?" +(x==y));
        System.out.println("x==z?" +(x==z));

        System.out.println("Operator !=");
        System.out.println("x!=y?" +(x!=y));
        System.out.println("x!=z?" +(x!=z));

        System.out.println("Operator >");
        System.out.println("x>y?" +(x>y));
        System.out.println("x>z?" +(x>z));

        System.out.println("Operator <");
        System.out.println("x<y?" +(x<y));
        System.out.println("x<z?" +(x<z));

        System.out.println("Operator >=");
        System.out.println("x>=y?" +(x>=y));
        System.out.println("x>=z?" +(x>=z));

        System.out.println("Operator <=");
        System.out.println("x<=y?" +(x<=y));
        System.out.println("x<=z?" +(x<=z));


    }
    
}
