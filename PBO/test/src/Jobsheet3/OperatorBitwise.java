package Jobsheet3;
public class OperatorBitwise {
    public static void main(String[] args) {
        int x = 2;
        int y = 12;

        System.out.println("Operator Bitwise");
        System.out.println((x << y));
        System.out.println((x >> y));
        System.out.println((x >> y));
    }
}