package Jobsheet3;

public class OperatorInDec {
    public static void main(String[] args) {
        int x;
            x=10;
        System.out.println("Perintah Increment");
        System.out.println("Pre increment ++x hasilnya "+ ++x);
        System.out.println("Nilai x =" + x);
        System.out.println("Post increment x++ hasil= "+ x++);
        System.out.println("Nilai x = "+ x);

        System.out.println("Perintah Decrement");
        System.out.println("Pre Decrement --x hasilnya "+ --x);
        System.out.println("Nilai x =" + x);
        System.out.println("Post decrement x-- hasil= "+ x--);
        System.out.println("Nilai x = "+ x);
    }
    
}
