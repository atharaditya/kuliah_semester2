package Jobsheet2;

public class BilanganBulat {
    public static void main(String[] args) {
        //deklarasi variabel
        byte nilaiA;
        short nilaiB;
        int hargaA;
        long hargaB;
        //inisialisasi variabel
        nilaiA= 97;
        nilaiB= 30000;
        hargaA= 2000000000;
        hargaB= 300000001;
        //cetak variabel
        System.out.println("Nilai A= "+nilaiA);
        System.out.println("Nilai B= "+nilaiB);
        System.out.println("Harga A= "+hargaA);
        System.out.println("Harga B= "+hargaB);
    }
}